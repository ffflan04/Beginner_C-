// メソッドの返り値を設定
// 返り値を設定することで関数として使うことができますね。

using System;

public class Lesson06
{
    public static void Main()
    {
        int num1 = Sum(3,2)
        Console.WriteLine(num1);

        int num2 = Sum(30, 20)
        Console.WriteLine(num2);
    }
    public static int Sum(int x, int y)
    {
        return x + y;
    }
}



